/* Copilot said: JavaScript é uma linguagem de programação essencial
JavaScript é uma linguagem de programação essencial para quem quer trabalhar com desenvolvimento web. Ela surgiu em 1995, criada por Brendan Eich em apenas 10 dias para o navegador Netscape, e rapidamente se tornou popular porque facilitou criar sites interativos.

Hoje, JavaScript é usado em todos os navegadores e é considerado um dos pilares da web, junto com HTML e CSS. Ele permite adicionar interatividade às páginas, como animações, efeitos e movimentos de objetos.

JavaScript é muito popular porque pode ser usado tanto no front-end (parte visual dos sites) quanto no back-end (servidores), graças a ferramentas como Node.js. Grandes empresas, como Google e Facebook, usam JavaScript em seus sistemas.

Além dos sites, JavaScript também serve para criar aplicativos de desktop, jogos, realidade virtual e até controlar dispositivos inteligentes, como geladeiras conectadas à internet.

Em resumo, aprender JavaScript é fundamental para quem quer ser programador ou desenvolvedor web, pois abre muitas oportunidades de trabalho e permite criar projetos em várias áreas da tecnologia.*/

/* ES6, também chamado de ECMAScript 6 ou ES2015, é uma versão do JavaScript lançada em 2015 que trouxe várias novidades para a linguagem. JavaScript existe desde 1995, mas foi padronizado pela organização Ecma International, que criou o ECMAScript para definir as regras da linguagem.

ES6 é considerado “JavaScript moderno” porque trouxe melhorias importantes, como novas formas de declarar variáveis (let e const), funções de seta (arrow functions), criação de classes, valores padrão em funções, promessas para lidar com ações assíncronas, entre outras funções.

Essas novidades tornam o código mais fácil de escrever e entender, além de ajudar quem já programa em outras linguagens a aprender JavaScript. ES6 também é muito usado em frameworks modernos como React.

Apesar disso, ainda existem muitos projetos usando versões antigas do JavaScript (pré-ES6), então é bom conhecer tanto o código antigo quanto o moderno para trabalhar em qualquer projeto.*/

